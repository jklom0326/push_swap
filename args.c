#include "push_swap.h"

static int	check_tab(char **t)
{
	int	i;
	int	j;

	i = 0;
	while (t[i])
	{
		j = 0;
		while (t[i][j])
		{
			if (t[i][j] == '-' || t[i][j] == '+')
				j++;
			if (!ft_isdifit(t[i][j]) || is_double(t) == 0 
			|| ft_atoi(&t[i][j]) <= -2147483648 
			|| ft_atoi(&t[i][j]) >= 2147483647)
			return (0);
			
		}
		
	}
	
}